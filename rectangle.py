def area(a, b):
    '''
    Принимает стороны прямоугольника:

    a : 1-я сторона
    b : 2-я сторона

    Возвращает площадь прямоугольника
    ''' 
    return a * b 

def perimeter(a, b):
    '''
    Принимает стороны прямоугольника:

    a : 1-я сторона
    b : 2-я сторона

    Возвращает периметр прямоугольника
    '''  
    return (a + b)*2